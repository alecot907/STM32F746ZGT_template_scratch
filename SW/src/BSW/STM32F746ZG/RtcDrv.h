#ifndef __RTCDRV_H
#define __RTCDRV_H

/*@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@*/
/* DEPENDENCIES */
/*@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@*/
#include "stm32f7xx.h"


/*@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@*/
/* TYPE definition */
/*@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@*/
typedef enum {
	FORBIDDEN = 0x0,
	MONDAY,
	TUESDAY,
	WEDNESDAY,
	THURSDAY,
	FRIDAY,
	SATURDAY,
	SUNDAY
} RTC_DayOfWeek_t;

typedef struct {
	uint8_t 						Year;
	uint8_t 						Month;
	uint8_t 						Day;
	RTC_DayOfWeek_t			DayOfWeek;
	uint8_t 						Hour;
	uint8_t 						Minute;
	uint8_t 						Second;
} RTC_Time_t;


/*@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@*/
/* PUBLIC FUNCTIONS */
/*@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@*/
void RTC_Init (void);

/**************************************************************************************/
/* RTC_ResetTime */
/**************************************************************************************/
void RTC_ResetTime (RTC_Time_t *time);

/**************************************************************************************/
/* RTC_SettTime */
/**************************************************************************************/
void RTC_SettTime (RTC_Time_t *time);

/**************************************************************************************/
/* RTC_GetTime */
/**************************************************************************************/
void RTC_GetTime (RTC_Time_t *time);


/*@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@*/
/* EXTERN VARIABLES */
/*@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@*/
extern RTC_Time_t RTC_Time;

#endif /* __RTCDRV_H */
